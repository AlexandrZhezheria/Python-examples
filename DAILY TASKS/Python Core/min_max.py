# Напиши функцію min_max, яка приймає список lst та повертає список з мінімальним та максимальним числом у заданому lst.
# lst завжди має хоча б один елемент.
# Приклад:
# min_max([1, 2, 3, 4, 5]) # повертає [1, 5]
# min_max([1]) # повертає [1, 1]
# min_max([1, 2, 3, 4, 5, 6, 10, 452, 21223, 312232, -20, 0, -12323]) # повертає [-12323, 312232]
# min_max([0, 0]) # повертає [0, 0]


def min_max(lst: list) -> list:
    return [min(lst), max(lst)]
