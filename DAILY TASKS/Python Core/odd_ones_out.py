# У тебе є список чисел. Кожне число в цьому списку може повторюватися певну кількість разів.
# Напиши функцію odd_ones_out, яка видаляє всі числа, що повторюються непарну кількість разів. Всі інші числа залиш як є.
# Приклади:
# odd_ones_out([26, 23, 24, 17, 23, 24, 23, 26]) == [26, 24, 24, 26]
# odd_ones_out([1, 2, 3, 1, 3, 3]) == [1, 1]
# odd_ones_out([1, 2, 3]) == []

def odd_ones_out(numbers: list) -> list:
    return [i for i in numbers if numbers.count(i) % 2 == 0]
