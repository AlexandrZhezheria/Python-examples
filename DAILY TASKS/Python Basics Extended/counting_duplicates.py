# Напиши функцію counting_duplicates, яка повертатиме кількість символів, які повторюються в рядку більше одного разу.
# Вхідний рядок містить лише буквенні (як великі, так і малі) та числові символи.
# Однакові літери у різних регістрах вважаються дублікатами.
# Приклади:
# counting_duplicates("abcde") == 0 # символи не повторюються більше одного разу
# counting_duplicates("aabbcde") == 2 # 'a' і 'b'
# counting_duplicates("aabBcde") == 2 # 'a' зустрічається двічі, 'b' зустрічається двічі (`b` і `B`)
# counting_duplicates("indivisibility") == 1 # "i" зустрічається шість разів
# counting_duplicates("Indivisibilities") == 2 # "i" зустрічається сім разів, а "s" зустрічається двічі
# counting_duplicates("aA11") == 2 # 'a' і '1'
# counting_duplicates("ABBA") == 2 # "A" і "B" зустрічаються двічі

def counting_duplicates(text: str) -> int:
    # Перетворюємо рядок в нижній регістр
    text = text.lower()

    # Створюємо порожній словник, де ключі - це символи в рядку, а значення - кількість входжень цього символу в рядок
    char_count = {}

    # Перебираємо кожен символ у рядку
    for char in text:
        # Якщо символ уже зустрічався у рядку, збільшуємо його лічильник на 1
        if char in char_count:
            char_count[char] += 1
        # Якщо символ ще не зустрічався, додаємо його до словника з лічильником 1
        else:
            char_count[char] = 1

    # Лічильник кількості дублікатів
    duplicate_count = 0

    # Перебираємо кожен символ із словника
    for char, count in char_count.items():
        # Якщо символ зустрічається більше одного разу, збільшуємо лічильник кількості дублікатів на 1
        if count > 1:
            duplicate_count += 1

    # Повертаємо кількість дублікатів
    return duplicate_count
